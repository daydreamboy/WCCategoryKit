#pragma mark - Generate Special Data

/*!
 *  Generate random NSData with specified size
 *
 *  @param length the number of bytes
 *
 *  @return the random NSData
 *
 *  @sa http://stackoverflow.com/questions/4917968/best-way-to-generate-nsdata-object-with-random-bytes-of-a-specific-length
 */
+ (NSData *)randomDataWithLength:(NSUInteger)length {
    NSMutableData *randomData = [NSMutableData dataWithCapacity:length];
    
    for (unsigned int i = 0; i < length / 4; ++i) {
        u_int32_t randomBits = arc4random();
        [randomData appendBytes:(void *)&randomBits length:4];
    }
    
    return randomData;
}

